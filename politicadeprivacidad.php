            <h3>4. Cookies</h3>
            <p>Utilizamos cookies para mejorar la experiencia del usuario en nuestro sitio. Puedes configurar tu navegador para rechazar las cookies, pero esto puede afectar algunas funcionalidades del sitio.</p>

